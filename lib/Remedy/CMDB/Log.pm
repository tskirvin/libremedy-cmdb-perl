package Remedy::CMDB::Log;

##############################################################################
### Configuration ############################################################
##############################################################################

our $NAME = "cmdb";

##############################################################################
### Declarations #############################################################
##############################################################################

use strict;
use warnings;

use Remedy::Log;

our @ISA = qw/Remedy::Log/;

##############################################################################
### Subroutines ##############################################################
##############################################################################

sub get_logger {
    my ($self) = @_;
    return $self->logger if (ref $self && $self->logger);
    return Log::Log4perl::get_logger ($NAME);
}

##############################################################################
### Final Documentation ######################################################
##############################################################################

1;
